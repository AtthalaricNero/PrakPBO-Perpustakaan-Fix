    private void btncariPegawaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariPegawaiActionPerformed
        // TODO add your handling code here:
        try {
            // Ambil teks dari TextField dan hapus spasi tambahan
            String input = txtIdPegawai.getText().trim();

            // Cek apakah input kosong
            if (input.isEmpty()) {
                namaPegawai.setText("Input tidak boleh kosong");
                return;
            }

            // Validasi input: coba konversi input ke angka
            int keyword = Integer.parseInt(input);

            // Panggil metode getById untuk mencari anggota berdasarkan ID
            Pegawai hasil = new Pegawai().getById(keyword);

            // Cek apakah hasil pencarian kosong
            if (hasil != null && hasil.getNama() != null) {
                // Tampilkan hanya nama anggota
                namaPegawai.setText("Nama: " + hasil.getNama());
            } else {
                namaPegawai.setText("Pegawai tidak ditemukan.");
            }
        } catch (NumberFormatException e) {
            // Tampilkan pesan jika input bukan angka
            namaPegawai.setText("Input harus berupa angka.");
        } catch (Exception e) {
            // Tangkap exception lain jika ada kesalahan
            namaPegawai.setText("Terjadi kesalahan");
            e.printStackTrace(); // Log error untuk debugging
        }
    }//GEN-LAST:event_btncariPegawaiActionPerformed

