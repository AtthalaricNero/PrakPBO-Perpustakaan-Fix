    private void tblPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamanMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblPeminjaman.getModel();
        int selectedRow = tblPeminjaman.getSelectedRow();
        Peminjaman pin = new Peminjaman();

        pin = pin.getById(Integer.parseInt(model.getValueAt(selectedRow, 0).toString()));

        txtIdPinjam.setText(String.valueOf(pin.getIdpeminjaman()));
        txtIdPegawai.setText(String.valueOf(pin.getPegawai().getIdpegawai()));
        txtIdAnggota.setText(String.valueOf(pin.getAnggota().getIdanggota()));
        txtIdBuku.setText(String.valueOf(pin.getBuku().getIdbuku()));

        txtTglPinjam.setText(pin.getTglpinjam());
        if (txtTglKembali.getText().isEmpty()) {
            setTanggalKembaliHariIni();
        } else {
            txtTglKembali.setText(pin.getTglkembali());
        }

        disableAllFieldsExceptTglKembali();
        enableBtnUpdate();
    }//GEN-LAST:event_tblPeminjamanMouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        // Validasi input field
        if (txtIdPegawai.getText().isEmpty() || txtIdAnggota.getText().isEmpty() || txtIdBuku.getText().isEmpty() || txtTglPinjam.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Gagal", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            Peminjaman pinjam = new Peminjaman();
            pinjam.setIdpeminjaman(Integer.parseInt(txtIdPinjam.getText()));
            pinjam.getPegawai().setIdpegawai(Integer.parseInt(txtIdPegawai.getText()));
            pinjam.getAnggota().setIdanggota(Integer.parseInt(txtIdAnggota.getText()));
            pinjam.getBuku().setIdbuku(Integer.parseInt(txtIdBuku.getText()));

            pinjam.setTglpinjam(txtTglPinjam.getText());
            pinjam.setTglkembali(txtTglKembali.getText());

            pinjam.save();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            kosongkanForm();
            tampilkanData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

